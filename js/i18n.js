// t(key) — returns translation for current language, falling back to English
window.t = (key) => {
  const lang = window.state && window.state.lang ? window.state.lang : 'en';
  return (window.translations[lang] && window.translations[lang][key]) || window.translations.en[key] || key;
};

// Applies translations to all elements carrying data-i18n / data-i18n-title attributes
window.applyTranslations = () => {
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    const val = window.t(key);
    if (val !== undefined) el.textContent = val;
  });
  document.querySelectorAll('[data-i18n-title]').forEach(el => {
    const key = el.getAttribute('data-i18n-title');
    el.setAttribute('title', window.t(key));
  });
  // Re-run active calculator so dynamic labels (Yr/Mo, comparison winner sentence) refresh
  if (window.refreshDynamicLabels) window.refreshDynamicLabels();
};

window.setLanguage = (lang) => {
  if (!window.translations[lang]) lang = 'en';
  window.state.lang = lang;
  window.saveState('lang', lang);
  document.documentElement.setAttribute('lang', lang);
  document.documentElement.setAttribute('dir', lang === 'ar' ? 'rtl' : 'ltr');
  const sel = document.getElementById('lang-select');
  if (sel) sel.value = lang;
  window.applyTranslations();
};
